const ICON_MAP: Record<string, React.ComponentType<IconProps>> = {
  cloud: CloudIcon,
  devops: DevOpsIcon,
  swe: SWEIcon,
  interview: InterviewIcon,
};

const ICON_COLOR_MAP: Record<string, string> = {
  cloud: "text-brand",
  devops: "text-warning",
  swe: "text-success",
  interview: "text-info",
};

export interface DomainIconProps {
  icon?: string;
  className?: string;
}

/**
 * Maps the optional `icon` string from exam-paths.json to an SVG icon component.
 * Defaults to the book icon when `icon` is absent or unrecognised.
 */
export function DomainIcon({ icon, className }: DomainIconProps) {
  const IconComponent = (icon && ICON_MAP[icon.toLowerCase()]) || DefaultIcon;
  const colorClass = (icon && ICON_COLOR_MAP[icon.toLowerCase()]) || "text-muted";

  return (
    <IconComponent
      aria-hidden={true}
      className={cn("h-5 w-5", colorClass, className)}
    />
  );
}
